iS 
     cnt number;
BEGIN
   if p_step = 0 then
        select count(*) into cnt from posts
        where group_no = p_group_no;
        --답변이 존재하면 update
        if cnt > 1 then
            update posts
            set del_Flag='Y'
            where POST_NO = p_POST_NO;
